
lista = ["Tomate", "Cebolla", "Huevos", "Leche", "Arroz"];
lista2 = [1, 2, 3, 4, 5];
lista3 = [6, 7, 8, 9, 10];
lista4 = ["f", "g", "a", "y", "p"];

#agregar datos a una lista
lista.append(50);
print(lista);

#eliminar datos de una lista, al saber el indice
del lista[3];
print(lista);

#unir la lista 3 a la lista 2, se unen
lista2.extend(lista3);
print(lista2);

#ordenar elementos de una lista
lista4.sort();
print(lista4);

#eliminar sin saber el indice
lista.remove("Arroz");
print(lista);