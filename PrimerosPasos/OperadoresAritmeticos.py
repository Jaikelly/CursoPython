#Operadores Aritmeticos

num1 = 100;
num2 = 10;

suma = num1 + num2;
resta = num1 - num2;
multi = num1 * num2;
div = num1 / num2;

print(suma);
print(resta);
print(multi);
print(div);

expo = num1 ** num2; #exponienciacion 
divEnt = num1 // num2; #division entera 
modulo = num1 % num2; #el resto de la division

print(expo);
print(divEnt);
print(modulo);